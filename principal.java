    private void bttcargarActionPerformed(java.awt.event.ActionEvent evt) {                                          
        // TODO add your handling code here:
        //dlgarchivosel f = new dlgarchivosel (this, true);
        //f.setVisible(true);
        JFileChooser jfc = new JFileChooser();
        FileNameExtensionFilter txtonly = new FileNameExtensionFilter("Archivos de Texto", "txt");
        jfc.setFileFilter(txtonly);
        jfc.setAcceptAllFileFilterUsed(false);
        int returnValue = jfc.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) 
        {
            File selectedFile = jfc.getSelectedFile();
            lblarchivosel.setText(selectedFile.getAbsolutePath());
            lastcontent = Grafo.ReadContent(selectedFile);
            TextoSel.setText(lastcontent);
            JOptionPane.showMessageDialog(null, "Recuerde guardar para mantener los cambios en la memoria", "RECORDATORIO",JOptionPane.INFORMATION_MESSAGE);
            btnsave.setEnabled(true);
            ///Boolean val = Grafo.WriteContent(lastcontent, Grafo.appfile);
            
	}
    }                                         

    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        Boolean val = Grafo.WriteContent(lastcontent, Grafo.appfile);
        if (val == true){
            JOptionPane.showMessageDialog(null, "Se ha guardado el contenido satisfactoriamente", "GUARDADO",JOptionPane.INFORMATION_MESSAGE);
            btnsave.setEnabled(false);
        }
        else
        { 
          JOptionPane.showMessageDialog(null, "Error al guardar el archivo", "ERROR",JOptionPane.ERROR_MESSAGE);  
        }

    }                                       
    
